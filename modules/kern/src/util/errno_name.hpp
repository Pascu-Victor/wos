            return "EINPROGRESS";
        case ESTALE:
            return "ESTALE";
        case EDQUOT:
            return "EDQUOT";
        case ECANCELED:
            return "ECANCELED";
        case EOWNERDEAD:
            return "EOWNERDEAD";
